/*Q.1 Write SQL query to print count of each user per segment and 
number of users who booked flight in April 2022*/

SELECT u.Segment, COUNT(DISTINCT u.User_id) AS Total_User_Count,
COUNT(DISTINCT CASE WHEN b.Line_of_business='Flight' AND
b.Booking_date BETWEEN '2022-04-01' AND '2022-04-30' 
THEN b.User_id END) AS User_who_booked_flight_in_Aprill2022
FROM dbo.user_table u
LEFT JOIN dbo.booking_table b
ON u.User_id=b.User_id
GROUP BY u.Segment


/*Q.2 SQL Query to report users whose first booking was a hotel booking*/
SELECT * 
FROM(
SELECT
*, RANK() OVER(PARTITION BY User_id ORDER BY Booking_date) as rn
FROM dbo.booking_table) a
WHERE rn=1 and Line_of_business='Hotel'


/*Q.3 SQL Query to report difference between first and last booking date of each user*/

SELECT User_id , DATEDIFF(DAY,MIN(Booking_date),MAX(Booking_date)) as No_of_Days
FROM dbo.booking_table
GROUP BY User_id


/*Q.4 SQL query to count the number of flight and hotel booking in each user segment*/

SELECT u.Segment, 
SUM(CASE WHEN b.Line_of_business='Flight' THEN 1 ELSE 0 END) AS Flight_Bookings,
SUM(CASE WHEN b.Line_of_business='Hotel' THEN 1 ELSE 0 END) AS Hotel_Bookings
FROM dbo.user_table u
LEFT JOIN dbo.booking_table b
ON u.User_id=b.User_id
GROUP BY u.Segment
